public class REE {
    private String gender;
    private String weight;
    private String height;
    private int age;

    //constructor
    public REE(String gender, String weight, String height, int age) {
        this.gender = gender;
        this.weight = weight;
        this.height = height;
        this.age = age;
    }

    // getter
    public String getGender() {
        return gender;
    }

    public String getWeight() {
        return weight;
    }

    public String getHeight() {
        return height;
    }

    public int getAge() {
        return age;
    }











}
